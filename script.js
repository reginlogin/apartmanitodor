// LANGUAGE SWITCH
// =========================
function setLang(lang){

  const t = translations[lang];

  document.getElementById("subtitle").innerText = t.subtitle;

  document.querySelector('a[href="#apartments"]').innerText = t.navApartments;
  document.querySelector('a[href="#house"]').innerText = t.navHouse;
  document.querySelector('a[href="#gallery"]').innerText = t.navGallery;
  document.querySelector('a[href="#location"]').innerText = t.navLocation;
  document.querySelector('a[href="#info"]').innerText = t.navInfo;
  document.querySelector('a[href="#contact"]').innerText = t.navContact;

  document.getElementById("apartmentsTitle").innerText = t.apartmentsTitle;
  document.getElementById("houseTitle").innerText = t.houseTitle;
  document.getElementById("galleryTitle").innerText = t.galleryTitle;
  document.getElementById("locationTitle").innerText = t.locationTitle;
  document.getElementById("infoTitle").innerText = t.infoTitle;
  document.getElementById("contactTitle").innerText = t.contactTitle;

  document.getElementById("emailBtn").innerText = t.emailBtn;
  document.getElementById("callBtn").innerText = t.callBtn;
  document.getElementById("footerText").innerText = t.footer;

  // rebuild apartments
  const container = document.getElementById("apartmentContainer");
  container.innerHTML = "";

  t.apartments.forEach(a=>{
    let card=document.createElement("div");
    card.className="card";
    card.innerHTML = `
      <h3>${a.name}</h3>
      <p><strong>Capacity:</strong> ${a.capacity}</p>
      <ul>${a.details.map(d=>`<li>${d}</li>`).join("")}
      </ul>
    `;
    container.appendChild(card);
  });
  // --- end apartments
  
  // rebuild house
  function renderHouse(lang) {
  const container = document.getElementById("houseContainer");
  container.innerHTML = "";
  const house = t.house;
  
  let card = document.createElement("div");
  card.className="card";  
  card.innerHTML = `
      <h3>${house.name}</h3>
      <p><strong>Capacity:</strong> ${house.capacity}</p>
      <ul>${house.details.map(d=>`<li>${d}</li>`).join("")}
      </ul>
    `;
  container.appendChild(card);
  }
  // --- end house

  // rebuild info
  function renderInfo(lang) {
  const container = document.getElementById("infoContainer");
  container.innerHTML = "";
  const info = t.info;
  
  let card = document.createElement("div");
  card.className = "card";
  card.innerHTML = info.map(d=>`<p>${d}</p>`).join("");
  container.appendChild(card);
  }
  // --- end info

  // initialize house section
  renderHouse(lang);

  // initialize info section
  renderInfo(lang);
  
  localStorage.setItem("lang",lang);
}




// Load saved language
const savedLang = localStorage.getItem("lang") || "en";
setLang(savedLang);



